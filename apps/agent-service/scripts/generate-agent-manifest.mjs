#!/usr/bin/env node
//
// Generate `src/agents/agents.generated.ts` from every AGENT.md under each
// `src/agents/<id>/` directory. The generated module is imported by the
// runtime so AGENT.md content is embedded in the Nitro bundle rather than
// read from disk — `.output/server/` doesn't include `src/agents/`.
//
// Runs as the predev / prebuild / preprepare / pretypecheck npm scripts.
// The output file is gitignored. Idempotent — re-running with no AGENT.md
// changes produces the same bytes.

import { readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const AGENTS_DIR = join(__dirname, "..", "src", "agents");
const OUT_FILE = join(AGENTS_DIR, "agents.generated.ts");

function findAgentFiles(rootDir) {
  let entries;
  try {
    entries = readdirSync(rootDir);
  } catch {
    return [];
  }
  const out = [];
  for (const name of entries) {
    const full = join(rootDir, name);
    let stat;
    try {
      stat = statSync(full);
    } catch {
      continue;
    }
    if (!stat.isDirectory()) continue;
    const agentMd = join(full, "AGENT.md");
    try {
      statSync(agentMd);
    } catch {
      continue;
    }
    out.push({
      path: relative(AGENTS_DIR, agentMd),
      content: readFileSync(agentMd, "utf-8"),
    });
  }
  out.sort((a, b) => a.path.localeCompare(b.path));
  return out;
}

const manifest = findAgentFiles(AGENTS_DIR);

const header = `// This file is auto-generated by scripts/generate-agent-manifest.mjs.
// Do not edit by hand. Re-run \`pnpm --filter agent-service prebuild\`.
`;

const body = `export const AGENT_MANIFEST: ReadonlyArray<{ path: string; content: string }> = ${JSON.stringify(
  manifest,
  null,
  2,
)} as const;
`;

writeFileSync(OUT_FILE, header + "\n" + body);

console.log(
  `[agents] generated ${manifest.length} entr${manifest.length === 1 ? "y" : "ies"} → ${relative(process.cwd(), OUT_FILE)}`,
);
